const commentSlider = document.querySelector('.comment-slider');
const slides = commentSlider.children[0].querySelectorAll('.swiper-slide');

swiperComment.on('transitionEnd', sliderCommentFilter);
window.addEventListener('load', sliderCommentFilter);

function sliderCommentFilter(e) {
  slides.forEach((element) => {
    let slideVisible = element.classList.contains('swiper-slide-visible');
    let slideNext = element.classList.contains('swiper-slide-next');

    if (slideVisible && slideNext) {
      element.style.transform = 'scale(1) translate3d(0%, 0%, 0px)';
      element.style.filter = 'blur(0)';
    } else {
      element.style.transform = 'scale(0.75) translate3d(0%, 0%, 0px)';
      element.style.filter = 'blur(2px)';
    }
  });
}

